# Sum
def sum (num1, num2):
    """
    add two numbers
    """
    return num1 + num2

# Subtraction
def sub (num1, num2):
    """
    subtract num2 from num1
    """
    return num1 - num2

# Multiplication
def mul (num1, num2):
    """
    Multiply two numbers
    """
    return num1 * num2

# Divide
def div(num1, num2):
    """
    Divide two non-zero number with result num1 divided by num2
    """
    return num1 / num2

